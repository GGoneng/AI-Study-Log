# 연습문제: 역삼각형 모양으로 별 출력하기
# 다음 소스 코드를 완성하여 역삼각형 모양으로 별이 출력되게 만드세요

for i in range(5):
    for j in range(5):
        if j < i:
            print(" ", end = "")
        else:
            print("*", end = "")
    print()

# 심사문제
# 표준 입력으로 삼각형의 높이가 입력됩니다. 입력된 높이만큼 산 모양으로 별을 출력하는 프로그램을 만드세요(input에서 안내 문자열은 출력하지 않아야 합니다). 
# 이때 출력 결과는 예제와 정확히 일치해야 합니다. 
# 모양이 같더라도 공백이나 빈 줄이 더 들어가면 틀린 것으로 처리됩니다.

height = int(input())

for i in range(height):
    for j in range(height * 2 - 1):
        if (j >= (height - i - 1) and j <= (height + i - 1)):
            print("*", end = "")
        else:
            print(" ", end = "")
    print()


